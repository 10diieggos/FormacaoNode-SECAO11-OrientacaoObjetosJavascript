class fileHandler {

  constructor (name) {
    this.file = name;
  };

  readFile () {
    console.log('Reading...');
  };

  writeFile (data) {
    console.log('Writing data...');
  };

  createFile (name) {
    console.log(`Creating file ${name}...`);
  };
  
  deleteFile (name) {
    console.log(`Deleting file ${name}...`);    
  };

};

